#Outputs line count, word count and character count in the file
wc file_name_01.txt

#Outputs only the line count in the file
wc -l file_name_01.txt

#Outputs only the byte/character count in the file
wc -c file_name_01.txt # for bytes
wc -m file_name_01.txt # for characters

#Outputs only the word count in the file
wc -w file_name_01.txt

#Outputs character count of the longest line in the file
wc -L file_name_01.txt
